package com.example.mylistview;

public class Niniz {
    private String name;
    private int imgID;

    Niniz(String name, int imgID) {
        this.name = name;
        this.imgID = imgID;
    }

    public String getName() {
        return name;
    }

    public int getImg() {
        return imgID;
    }
}
